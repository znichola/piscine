#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char **argv)
{
	while (1 < argc)
	{
		argc--;
		ft_putstr(argv[argc]);
		write(1, "\n", 1);
	}
}
